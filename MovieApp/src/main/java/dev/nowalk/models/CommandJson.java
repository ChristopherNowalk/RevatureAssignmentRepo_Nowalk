package dev.nowalk.models;

public class CommandJson {

	private String command;

	public CommandJson() {
		super();
	}

	public CommandJson(String command) {
		super();
		this.command = command;
	}

	public String getCommand() {
		return command;
	}

	public void setCommand(String command) {
		this.command = command;
	}
	
	
}
