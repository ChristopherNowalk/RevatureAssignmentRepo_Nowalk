
public class Loops {
	public static void main(String[] args)
	{
		//a simple loop that prints the numbers 1-10
		for(int i = 1; i <= 10; i++)
		{
			System.out.println(i);
		}
		//it is important to know that you cannot access the variable i outside of the for loop
		//as it does not exist outside of the scope of the loop
	}
}
